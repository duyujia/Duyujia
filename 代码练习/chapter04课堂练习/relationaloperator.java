public class relationaloperator {
	
	//编写一个main方法
	public static void main(String[] args) {
		int a = 9;
		int b = 8;
		System.out.println(a > b);
		System.out.println(a >= b);
		System.out.println(a <= b);
		System.out.println(a < b);
		System.out.println(a == b);
		System.out.println(a != b);
		boolean flag = a > b;
		System.out.println("flag=" + flag);
		
	}
}